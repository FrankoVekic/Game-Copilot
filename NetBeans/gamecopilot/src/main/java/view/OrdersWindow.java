     private void load() {
        table = (DefaultTableModel) jTable1.getModel();
        List<OrderList> entities = controller.read();
        String s;
        for (OrderList p : entities) {
            s="";
            Object[] o = new Object[7];
            o[0] = p.getId();
            o[1] = p.getOrderDate();
            o[2] = p.getUser();
            o[3] = p.getAddress();
            o[4] = p.getCity();
            o[5] = p.getCountry();
            for(ProductOrder pr : p.getProducts()){
                s+=pr.getProduct().getName();
            }
            o[6] = (s);
            table.addRow(o);
        }

    }

